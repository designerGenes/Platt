import UIKit

extension UIFont {
    class func sfProTextBold(size: CGFloat) -> UIFont {
        return UIFont(name: "SFProText-Bold", size: size)!
    }
    
    class func sfProTextRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "SFProText-Regular", size: size)!
    }
}
